public class IntegerBinaryTreeNode {
    private int data;
    private IntegerBinaryTreeNode leftNode;
    private IntegerBinaryTreeNode rightNode;

    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }

    public IntegerBinaryTreeNode getLeftNode() {
        return leftNode;
    }

    public void setLeftNode(IntegerBinaryTreeNode leftNode) {
        this.leftNode = leftNode;
    }

    public IntegerBinaryTreeNode getRightNode() {
        return rightNode;
    }

    public void setRightNode(IntegerBinaryTreeNode rightNode) {
        this.rightNode = rightNode;
    }
}
